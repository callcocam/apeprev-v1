    <h2 class="text-primary font-bold text-3xl">Apresentação</h2>
    <p class="text-justify pb-4 pt-4">
        A Associação Paranaense das Entidades Previdenciárias do Estado e dos Municípios - APEPREV, foi criada
        em
        junho de 2002, com intuito educacional, a qual mantém até hoje, fazendo disso sua principal força e
        característica, preocupando-se principalmente com a formação e capacitação de seus associados,
        proporcionando às instituições, constante aprimoramento de conhecimento através de atividades de
        intercâmbio
        estaduais e nacionais, realizando congressos, workshops, cursos e seminários com o objetivo de discutir
        e
        difundir os princípios previdenciários, devido as várias mudanças na Legislação Previdenciária, pois a
        cada
        dia que passa surgem inovações, sejam nos aspectos legais, políticos ou econômicos. Desse modo, a
        participação cada vez mais pró-ativa de todos os envolvidos é fundamental para o crescimento da
        Associação e
        dos associados. Temos no estado do Paraná 177 municípios com Regime Próprio de Previdência Social
        (RPPS).
        Segundo dados da Secretaria de Previdência - Ministério da Economia, esses municípios juntos superam um
        patrimônio líquido (PL) acima de R$ 15 bilhões de reais. A APEPREV atua como representante das
        instituições
        de previdência dos Municípios e Estado do Paraná, que são responsáveis pela gestão dos RPPS, assim sendo
        a
        APEPREV promove importantes fóruns de debates democráticos que difundem informações, cultura e
        capacitações
        previdenciárias, refletindo na possibilidade de uma nova visão de previdência para todo o Estado do
        Paraná.
        A APEPREV continua se aprimorando cada vez mais, buscando diariamente e incansavelmente a
        profissionalização
        dos Regimes Próprios de Previdência Social, com o intuito de fortalecer ainda mais os RPPS do estado do
        Paraná, continuando a formação e organização de encontros regionais, grupos de estudos e debates que
        favoreçam troca de experiências. A APEPREV tem o prazer em convidar os Regimes Próprios de Previdência
        Social Municipais do Estado do Paraná para que, através de seus Gestores e Servidores, venham se juntar
        a
        nós neste processo democrático de discussão e disseminação de informações, do associativismo como forma
        de
        romper barreiras e diminuir distâncias para promover mudanças no atual contexto previdenciário e fazer
        dos
        RPPS’s paranaenses instituições sólidas e respeitadas
    </p>

